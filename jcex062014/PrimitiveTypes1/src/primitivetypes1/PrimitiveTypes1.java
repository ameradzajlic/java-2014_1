package primitivetypes1;
public class PrimitiveTypes1 {
    public static void main(String[] args) {
        int x = 10;
        int y = 010;
        int z = 0x10;
        int i = 0b10;
        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
        System.out.println(i);
    }
    
}
